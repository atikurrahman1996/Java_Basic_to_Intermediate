package JavawithMosh;

public class SimplifyingIfStatement16 {

	public static void main(String[] args) {

		
		int income = 1200;
		boolean hasHighIncome;
		
		if(income > 1000)
		{
		 
			hasHighIncome = true;
		}
		
		else
		{
			hasHighIncome = false;
		}
		
		System.out.println(hasHighIncome);
		
		
	}

}
	
